print('Hi Python')
print("Hi Python")
print('"Hi Python"')
print("'Hi Python'")
print("\"Hi Python\"")   #""""큰 따옴표 안에 큰 따옴표 표시
print('Hi Python, '*10)  # *10은 10번 출력

print('Hi\
 Python')                #\는 둘개의 줄을 하나의 줄로 취급
print('\\20,000')        #\\는 \자체 출력
print('Hi \nPython')     #\n은 줄을 바꾸어 출력
print('Hi \t\t Python')  #\t는 탭만큼 띄우고 출력